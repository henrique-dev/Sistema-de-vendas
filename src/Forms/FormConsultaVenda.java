    public void listar(){
        try{
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"Nome","Data da venda","Vencimento","Valor"});
            RepositorioVenda rep = RepositorioVenda.obterInstancia();
            this.lista = rep.listar();
            templista = new ArrayList<>();
            templista = lista;
            for(int x=0;x<lista.size();x++){
                modelo.addRow(new Object[]{this.lista.get(x).getCliente().getNome(),
                    this.lista.get(x).getDataVenda(),this.lista.get(x).getDataVencimento(),
                    "R$ "+ Double.parseDouble(this.lista.get(x).getTotalVenda())
                });
            }
        jTable1.setModel(modelo);
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
    
