    public void listar(){
        try{
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Nome","CPF","Endere√ßo","Bairro"});
        RepositorioCliente rep = RepositorioCliente.obterInstancia();
        this.lista = rep.listar();
        templista = new ArrayList<>();
        templista = lista;
        for(int x=0;x<lista.size();x++){
            modelo.addRow(new Object[]{this.lista.get(x).getNome(),this.lista.get(x).getCpf(),
                this.lista.get(x).getEndereco(),this.lista.get(x).getBairro()});
        }
        jTable1.setModel(modelo);
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }

