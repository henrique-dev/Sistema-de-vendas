    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String temp;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Nome","CPF","Endere√ßo","Bairro"});
        String nomePesquisa = jTextField1.getText().toLowerCase();
        boolean status = false;

        for(int a=0;a<lista.size();a++){
            temp = lista.get(a).getNome().toLowerCase();

            if(temp.startsWith(nomePesquisa)){

                if(!status){
                    templista = new ArrayList<>();
                }

                templista.add(lista.get(a));
                status = true;
                modelo.addRow(new Object[]{this.lista.get(a).getNome(),this.lista.get(a).getCpf(),
                    this.lista.get(a).getEndereco(),this.lista.get(a).getBairro()});

        }

        if(status){
            jTable1.setModel(modelo);
        }
        if(!status){
            jTable1.setModel(modelo);
        }

        }

    }//GEN-LAST:event_jButton4ActionPerformed

